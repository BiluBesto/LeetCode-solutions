class Solution {
public:
    string toLowerCase(string s) {
        string res = "";
        char temp = ' ';
        for(char c:s)
        {   temp = c;
            if(c-'Z'<=0 && (c=='A'||c=='B'||c=='C'||c=='D'||c=='E'||c=='F'||c=='G'||c=='H'||c=='I'||c=='J'||c=='K'||c=='L'||c=='M'||c=='N'||c=='O'||c=='P'||c=='Q'||c=='R'||c=='S'||c=='T'||c=='U'||c=='V'||c=='W'||c=='X'||c=='Y'||c=='Z'))
            {
                temp = c+32;
            }
            res+=temp;
        }
        return res;
    }
};

